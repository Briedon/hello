public class newClass {
int a,b,c,d;
    public newClass() {
    }

    @Override
    public String toString() {
        return "newClass{" + "a=" + a + ", b=" + b + ", c=" + c + ", d=" + d + '}';
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }
    
}
